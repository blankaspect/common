// PACKAGE


package uk.blankaspect.common.dataio;

//----------------------------------------------------------------------


// IMPORTS


import java.io.IOException;

//----------------------------------------------------------------------


// INTERFACE: BYTE DATA SOURCE


public interface IByteDataSource
	extends IDataInput
{

////////////////////////////////////////////////////////////////////////
//  Member classes : non-inner classes
////////////////////////////////////////////////////////////////////////


	// CLASS: BYTE DATA


	public static class ByteData
	{

	////////////////////////////////////////////////////////////////////
	//  Instance variables
	////////////////////////////////////////////////////////////////////

		private	byte[]	data;
		private	int		offset;
		private	int		length;

	////////////////////////////////////////////////////////////////////
	//  Constructors
	////////////////////////////////////////////////////////////////////

		public ByteData(byte[] data)
		{
			this.data = data;
			length = data.length;
		}

		//--------------------------------------------------------------

		public ByteData(byte[] data,
						int    offset,
						int    length)
		{
			this.data = data;
			this.offset = offset;
			this.length = length;
		}

		//--------------------------------------------------------------

	////////////////////////////////////////////////////////////////////
	//  Instance methods
	////////////////////////////////////////////////////////////////////

		public byte[] getData()
		{
			return data;
		}

		//--------------------------------------------------------------

		public int getOffset()
		{
			return offset;
		}

		//--------------------------------------------------------------

		public int getLength()
		{
			return length;
		}

		//--------------------------------------------------------------

	}

	//==================================================================

////////////////////////////////////////////////////////////////////////
//  Methods
////////////////////////////////////////////////////////////////////////

	ByteData getData()
		throws IOException;

	//------------------------------------------------------------------

}

